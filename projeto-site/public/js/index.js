//MENU
function mudaNomeNav() {
   navegação.className = "nav";
}
function some() {
   if (navegação.onmouseout) {
      navegação.className = "nav_none";
   } else {
      navegação.className = "nav";
   }
}
var contador = 1;
function sublink() {
   if (contador % 2 == 0) {
      sub.style.display = 'none';
   } else {
      sub.style.display = 'block';
   }
   contador++;
}

